const sum = (num1, num2) => num1 + num2

const sub = (num1, num2) => num1 - num2

const mult = (num1, num2) => num1 * num2

const divide = (num1, num2) => num1 / num2


const squareRoot = (num) => Math.sqrt(num)

const maxNum = (num1, num2) => Math.max(num1, num2)

module.exports = {
    sum,
    sub,
    mult,
    divide,
    squareRoot,
    maxNum
}